#include "libft.h"

char	*ft_strcat(char *str1, const char *str2)
{
	size_t	i;
	size_t	k;

	k = 0;
	i = 0;
	while (str1[i] != '\0')
	{
		i++;
	}
	while (str2[k] != '\0')
	{
		str1[i] = str2[k];
		i++;
		k++;
	}
	str1[i] = '\0';
	return (str1);
}
